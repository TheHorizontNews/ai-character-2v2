// Function to get cluster pages for a given page
export function getClusterPages(currentSlug) {
  for (const [clusterName, cluster] of Object.entries(seoPageClusters)) {
    if (cluster.pages.includes(currentSlug)) {
      return {
        clusterName,
        pages: cluster.pages.filter(p => p !== currentSlug),
        anchorVariations: cluster.anchorVariations
      };
    }
  }
  return null;
}

// Function to get a random anchor for a page (for variety)
export function getRandomAnchor(pageSlug, anchorVariations, usedAnchors = []) {
  const anchors = anchorVariations[pageSlug];
  if (!anchors || anchors.length === 0) return null;
  
  // Filter out already used anchors
  const availableAnchors = anchors.filter(a => !usedAnchors.includes(a.toLowerCase()));
  
  if (availableAnchors.length === 0) {
    // If all anchors used, return first one
    return anchors[0];
  }
  
  // Return random anchor from available ones
  const randomIndex = Math.floor(Math.random() * availableAnchors.length);
  return availableAnchors[randomIndex];
}
